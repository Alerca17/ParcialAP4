package Punto1;

public class Numeros {

    private String num_1, num_2, num_3;

    public Numeros() {
    }

    public Numeros(String num_1, String num_2, String num_3) {

        this.num_1 = num_1;
        this.num_2 = num_2;
        this.num_3 = num_3;
    }

    public String getNum_1() {
        return num_1;
    }

    public void setNum_1(String num_1) {
        this.num_1 = num_1;
    }

    public String getNum_2() {
        return num_2;
    }

    public void setNum_2(String num_2) {
        this.num_2 = num_2;
    }

    public String getNum_3() {
        return num_3;
    }

    public void setNum_3(String num_3) {
        this.num_3 = num_3;
    }
}
